#include <cassert>

#include "value.h"
#include "opcode.h"

namespace Charly {

void Object::init(uint32_t initial_capacity, Class* klass) {
  Container::init(kTypeObject, initial_capacity);
  this->klass = klass;
}

void Object::init(Object* source) {
  Container::init(source);
  this->klass = source->klass;
}

Class* Object::get_klass() {
  return this->klass;
}

}  // namespace Charly
